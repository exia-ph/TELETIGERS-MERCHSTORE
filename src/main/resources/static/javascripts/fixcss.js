window.onload = function()
{ 
    var elements = document.getElementsByClassName('row');

    for (var i = 0; i < elements.length; i++) 
    {
        var element = elements[i];
        element.style.fontSize = "20px";
    }
    
    for (var i = 0; i < elements.length; i++) 
    {
        var element = elements[i];
        element.style.fontSize = "74px";
    }
}

